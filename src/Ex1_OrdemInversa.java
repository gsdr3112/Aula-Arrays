import java.util.Arrays;

public class Ex1_OrdemInversa {
    public static void main(String[] args) {
        array1();
    }

    public static void array1() {
        int[] array = {1, 2, 3, 4, 5, 6};

        for (int i = array.length - 1; i >= 0; i--) {
            System.out.println(array[i]);

        }

    }


}
